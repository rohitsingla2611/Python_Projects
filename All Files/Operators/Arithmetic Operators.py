# Arithmetic Operators
num1 = 30
num2 = 20
num3 = 22
num4 = 7
num5 = 5

print("Addition Operation  ", num1, " + ", num2, " = ", num1 + num2)
print("Subtraction Operation  ", num1, " - ", num2, " = ", num1 - num2)
print("Multiplication Operation  ", num1, " * ", num2, " = ", num1 * num2)
print("Division / Operation  ", num1, " / ", num2, " = ", num1 / num2)
print("Power Operation  ", num5, '**', num4, " = ", num5 ** num4)
print("Remainder Operation  ", num2, " % ", num4, " = ", num2 % num4)
print("Division // Operation  ", num3, " // ", num4, " = ", num3 // num4)

