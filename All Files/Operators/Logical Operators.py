# Logical Operators
# It is used for more than one conditions

num1 = 30
num2 = 20
num3 = 22
num4 = 7
num5 = 5

print("And Operator 'and' ", num1, '>', num2, 'and', num1, '>', num3, " ==> ", num1 > num2 and num1 > num3)
print("Or Operator 'or'  ", num1, '>', num2, 'or', num1, '>', num3, " ==> ", num1 > num2 or num1 > num3)
print("Not Operator 'not'  ", ' not', num1, '>', num3, " ==> ", not num1 > num3)
