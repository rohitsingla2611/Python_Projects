# Bitwise Operators
num1 = 30  # 11110
num2 = 20  # 10100
num3 = 22  # 10110
num4 = 7  # 00111
num5 = 5  # 00101
num6 = 1  # 00001
print("OR Operator '|'  ", num1, " | ", num4, " = ", num1 | num4)
print("AND Operator '&'  ", num1, " & ", num5, " = ", num1 & num5)
print("XOR Operator '^'  ", num1, " ^ ", num2, " = ", num1 ^ num2)
print("Right Shift '>>'  ", num1, " >> ", num6, " = ", num1 >> num6)
print("Left Shift '<<'  ", num4, " << ", num5, " = ", num4 << num5)
