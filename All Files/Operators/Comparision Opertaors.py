# Comparision Operators
num1 = 30
num2 = 20
num3 = 22
num4 = 7
num5 = 5

print("Greater Than '>'  ", num1, " > ", num2, " = ", num1 > num2)
print("Smaller Than '<'  ", num1, " < ", num2, " = ", num1 < num2)
print("Equal to '=='  ", num1, " == ", num2, " = ", num1 == num2)
print("Not Equal to '!='  ", num1, " != ", num2, " = ", num1 != num2)
