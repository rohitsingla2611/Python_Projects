num1 = int(input('Enter a value of Day from 1 to 7 '))

if num1 == 1:
    print("It's Monday")
elif num1 == 2:
    print("It's Tuesday")
elif num1 == 3:
    print("It's Wednesday")
elif num1 == 4:
    print("It's Thursday")
elif num1 == 5:
    print("It's Friday")
elif num1 == 6:
    print("It's Saturday")
elif num1 == 7:
    print("It's Sunday")
else:
    print('Wrong Input')
